package DSA.Arrays.Medium;

import java.util.Arrays;

public class Occurrences {
    public boolean uniqueOccurrences(int[] arr) {
        int n = arr.length-1;
        int[] ans = new int[n];
        Arrays.fill(ans, 0);
        for(int i =0; i<n; i++){
            int val = arr[i];
        }
    }
}
